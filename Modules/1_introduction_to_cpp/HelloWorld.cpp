// This line includes the standard input-output stream library.
// It allows us to use functions and objects for input-output operations.
#include<iostream>

// The main function is where the program starts executing.
int main()
{
    // The 'cout' object is used to display output to the console.
    // Here, we output the string "Hello, World!".
    std::cout << "Hello, World!" << std::endl;

    // This line signifies the successful termination of the program.
    // It returns an exit status of 0.
    return 0;
}
